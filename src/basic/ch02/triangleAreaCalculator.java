package basic.ch02;

public class triangleAreaCalculator {
	
	public static void main(String[] args) {
		
		final int BASE = 5;
		final int HEIGHT = 4;
		
		
		// 결과값을 area 변수에 저장
		int area =  (BASE * HEIGHT)/2; // 수학처럼 + - * / 의 우선순위를 정할 수 있음.
		
		System.out.println(area);
		
//		string 문자열과 함께 나타낼 수 있음.
		
	}

}
